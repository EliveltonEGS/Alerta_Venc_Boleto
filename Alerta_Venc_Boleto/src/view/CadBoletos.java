    // carrega tabela principal
    private void carregaTabela(String status_pgto) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("BENEFICIÁRIO");
        modelo.addColumn("VALOR");
        modelo.addColumn("DATA VENCIMENTO");
        modelo.addColumn("Status PGTO");

        bdao = new BoletosDAO();
        for (Boletos b : bdao.listarBoletosNaoPagos("NÃO")) {
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            String dataFormatada = formato.format(b.getDtVenc());

            modelo.addRow(new Object[]{b.getCodigo(), b.getBeneficiario(), 
                Double.toString(b.getValor()).replace(".", ","), dataFormatada, b.getStatus()});
        }

        tbBoletos.setModel(modelo);
    }

