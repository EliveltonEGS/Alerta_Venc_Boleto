    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtBeneficiario.getText().isEmpty() || txtDataVenc.getText().isEmpty()
                || txtValor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os todos campos com (*)",
                    "Atenção", JOptionPane.ERROR_MESSAGE);
        } else {
            boletos = new Boletos();
            bdao = new BoletosDAO();

            //formata data para salvar no banco
            String dia = txtDataVenc.getText().substring(0, 2);
            String mes = txtDataVenc.getText().substring(3, 5);
            String ano = txtDataVenc.getText().substring(6);

            String dataSalva = ano + "-" + mes + "-" + dia;
            Date dataVenc = new Date();
            SimpleDateFormat dtFormatada = new SimpleDateFormat("yyyy-MM-dd");

            try {
                dataVenc = dtFormatada.parse(dataSalva);
            } catch (ParseException ex) {
                Logger.getLogger(CadBoletos.class.getName()).log(Level.SEVERE, null, ex);
            }

            boletos.setBeneficiario(txtBeneficiario.getText().toUpperCase());
            boletos.setValor(Double.parseDouble(txtValor.getText().replace(",", ".")));
            boletos.setStatus(txtStatus.getText());
            boletos.setDtVenc(dataVenc);

            bdao.merge(boletos);

            this.desabilitaCampos();
            btnSalvar.setVisible(false);
            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
            this.limpaCampos();
            this.carregaTabela("NÃO");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

